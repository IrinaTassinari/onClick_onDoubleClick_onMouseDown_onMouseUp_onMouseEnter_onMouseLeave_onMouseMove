import { useState } from "react";
import './ImageCarousel.css'

function ImageCarousel(){
 //массив изобр-й
 const images = [
    "https://images.unsplash.com/photo-1503023345310-bd7c1de61c7d",
    "https://images.unsplash.com/photo-1529626455594-4ff0802cfb7e",
    "https://images.unsplash.com/photo-1524253482453-3fed8d2fe12b",
    "https://picsum.photos/id/237/800/600"
 ];
 // cocт-е для хранения текущего индекса
 const [index, setIndex] = useState(0);

//ф-я переключения изображений вперед
const handleNext = () => {
    setIndex((prevIndex) => (prevIndex+1) % images.length)
                        //(0+1)%4 = 1
                        //(1+1)%4 = 2
                        //(2+1)%4 = 3
                        //(3+1)%4 = 0
}

// Функция переключения изображений назад
const handlePrev = () => {
    setIndex((prevIndex) => (prevIndex-1 + images.length) % images.length)
}
//(prevIndex-1 + images.length)→ гарантирует, что индекс никогда не станет отрицательным. (0 - 1 + 4) = 3
//Знак % — это оператор остатка от деления, который позволяет сделать переход "по кругу". Он превращает индекс в циклический.

return (
        <div className="carousel">
            <img
                src={images[index]}
                alt={`Изображение ${index + 1}`}
                className="carousel-image"
            />
            <div>
                <button onClick={handlePrev} className="carousel-btn">
                Назад
            </button>
            <button onClick={handleNext} className="carousel-btn">
                Следующее
            </button>
            </div>
        </div>
    )
}
export default ImageCarousel;



